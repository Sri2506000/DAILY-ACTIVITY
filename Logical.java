import java.util.*;
 public class Logical{
 public static void main(String args[])
 {

   System.out.println((5>3)&&(8>5));
   System.out.println((5<3)&&(5<8));
   System.out.println((5<3)||(8>5));
   System.out.println((5<3)||(8<5));
   System.out.println(!(5==3));
   System.out.println(!(8<5));
}
}